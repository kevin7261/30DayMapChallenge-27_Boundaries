          console.error('[MapTab] å°ç£ç¸£å¸‚æ•¸æ“šè¼‰å…¥å¤±æ•—:', error);
          return false;
        }
      };

      /**
       * ğŸ—ºï¸ ç¹ªè£½å°ç£ç¸£å¸‚
       */
      const drawCounties = () => {
        if (!g || !countyData.value) {
          console.error('[MapTab] ç„¡æ³•ç¹ªè£½ç¸£å¸‚: g=', !!g, 'countyData=', !!countyData.value);
          return;
        }

        try {
          console.log('[MapTab] é–‹å§‹ç¹ªè£½å°ç£ç¸£å¸‚ GeoJSON');

          // ç¹ªè£½æ‰€æœ‰ç¸£å¸‚
          g.selectAll('.county')
            .data(countyData.value.features)
            .enter()
            .append('path')
            .attr('d', path)
            .attr('class', 'county')
            .attr('fill', '#FFFFFF') // ç™½è‰²å¡«å……
            .attr('fill-opacity', 0.8)
            .attr('stroke', '#000000') // é»‘è‰²é‚Šæ¡†
            .attr('stroke-width', 1.5);

          console.log('[MapTab] å°ç£ç¸£å¸‚ GeoJSON ç¹ªè£½å®Œæˆ');
        } catch (error) {
          console.error('[MapTab] å°ç£ç¸£å¸‚ GeoJSON ç¹ªè£½å¤±æ•—:', error);
        }
      };

      /**
       * ğŸ—ï¸ å‰µå»ºåœ°åœ–å¯¦ä¾‹
       * åˆå§‹åŒ– D3.js åœ°åœ–ä¸¦è¨­å®šåŸºæœ¬é…ç½®
       */
      const createMap = () => {
        if (!mapContainer.value) return false;

        const rect = mapContainer.value.getBoundingClientRect();
        if (rect.width === 0 || rect.height === 0) {
          console.warn('[MapTab] å®¹å™¨å°ºå¯¸ç‚ºé›¶ï¼Œå»¶é²åˆå§‹åŒ–');
          return false;
        }

        try {
          const width = rect.width;
          const height = rect.height;

          // å°ç£ä¸­å¿ƒä½ç½®ï¼šç·¯åº¦ 23.5Â°, ç¶“åº¦ 121Â°

          // å‰µå»º SVG å…ƒç´ 
          svg = d3
            .select(mapContainer.value)
            .append('svg')
            .attr('width', width)
            .attr('height', height)
            .style('background', '#808080'); // ç°è‰²èƒŒæ™¯

          // å‰µå»ºæŠ•å½± - éº¥å¡æ‰˜æŠ•å½±ï¼Œèšç„¦åœ¨å°ç£
          projection = d3
            .geoMercator()
            .center([121, 23.5]) // ä¸­å¿ƒé»åœ¨å°ç£
            .scale(6000) // è¼ƒå¤§çš„ç¸®æ”¾æ¯”ä¾‹ï¼Œèšç„¦åœ¨å°ç£
            .translate([width / 2, height / 2]);

          // å‰µå»ºè·¯å¾‘ç”Ÿæˆå™¨
          path = d3.geoPath().projection(projection);

          // å‰µå»ºå®¹å™¨çµ„
          g = svg.append('g');

          // è¨­ç½®ç¸®æ”¾è¡Œç‚º
          zoom = d3
            .zoom()
            .scaleExtent([1, 20]) // å…è¨±ç¸®æ”¾ 1x åˆ° 20x
            .on('zoom', (event) => {
              g.attr('transform', event.transform);
            });

          svg.call(zoom);

          isMapReady.value = true;

          // å°‡åœ°åœ–å¯¦ä¾‹å‚³éçµ¦çˆ¶çµ„ä»¶
          emit('map-ready', { svg, projection, path });

          console.log('[MapTab] D3.js åœ°åœ–å‰µå»ºæˆåŠŸ');
          return true;
        } catch (error) {
          console.error('[MapTab] D3.js åœ°åœ–å‰µå»ºå¤±æ•—:', error);
          return false;
        }
      };

      /**
       * ğŸš€ åˆå§‹åŒ–åœ°åœ–
       * å‰µå»ºåœ°åœ–ä¸¦è¼‰å…¥åˆå§‹æ•¸æ“š
       */
      const initMap = async () => {
        let attempts = 0;
        const maxAttempts = 20;

        // è¼‰å…¥å°ç£ç¸£å¸‚æ•¸æ“š
        const loaded = await loadCountyData();
        if (!loaded) {
          console.error('[MapTab] ç„¡æ³•è¼‰å…¥å°ç£ç¸£å¸‚æ•¸æ“š');
          return;
        }

        const tryCreateMap = async () => {
          if (attempts >= maxAttempts) {
            console.error('[MapTab] åœ°åœ–åˆå§‹åŒ–å¤±æ•—ï¼Œå·²é”åˆ°æœ€å¤§å˜—è©¦æ¬¡æ•¸');
            return;
          }

          attempts++;
          console.log(`[MapTab] å˜—è©¦å‰µå»ºåœ°åœ– (${attempts}/${maxAttempts})`);

          if (createMap()) {
            console.log('[MapTab] åœ°åœ–å‰µå»ºæˆåŠŸï¼Œé–‹å§‹ç¹ªè£½åœ–å±¤');
            // ç¹ªè£½å°ç£ç¸£å¸‚
            drawCounties();
          } else {
            console.log('[MapTab] åœ°åœ–å‰µå»ºå¤±æ•—ï¼Œ100ms å¾Œé‡è©¦');
            setTimeout(tryCreateMap, 100);
          }
        };

        tryCreateMap();
      };

      // ğŸ§¹ ç”Ÿå‘½é€±æœŸï¼šçµ„ä»¶æ›è¼‰
      onMounted(() => {
        nextTick(() => {
          initMap();
        });
      });

      // ğŸ§¹ ç”Ÿå‘½é€±æœŸï¼šçµ„ä»¶å¸è¼‰
      onUnmounted(() => {
        if (svg) {
          svg.remove();
          svg = null;
        }

        projection = null;
        path = null;
        zoom = null;
        g = null;
        isMapReady.value = false;
      });

      // ğŸ“¤ è¿”å›çµ„ä»¶å…¬é–‹çš„å±¬æ€§å’Œæ–¹æ³•
      return {
        mapContainer,
        mapContainerId,
      };
    },
  };
</script>

